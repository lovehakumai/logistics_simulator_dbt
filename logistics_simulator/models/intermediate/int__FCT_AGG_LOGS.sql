WITH base AS (
    SELECT * FROM {{ref('int__FCT_LOGS_RAW')}}
)
, latest_3_month AS (
    SELECT
        ORIGINAL_BASE_POINT
        , DESTINATION_BASE_POINT
        , TRANSPORT_MODE 
        , AVG(GEOGRAPHICAL_RISK_SCORE) AS DESTINATION_RISK_AVG
        , MODE(WEATHER_CONDITION) AS WEATHER_CONDITION
        , AVG(LEAD_TIME_DAYS) AS LEAD_TIME_DAYS
        , COUNT( CASE WHEN DISRUPTION_OCCURRED = 1 THEN 1 END ) / COUNT(1) AS DISRUPTION_RATE
    FROM base 
    WHERE DATE >= DATEADD(MONTH, -3, (SELECT MAX(DATE) FROM base))
    GROUP BY 
        ORIGINAL_BASE_POINT
        , DESTINATION_BASE_POINT
        , TRANSPORT_MODE
)
, whole_period AS (
    SELECT
        ORIGINAL_BASE_POINT
        , DESTINATION_BASE_POINT
        , TRANSPORT_MODE
        , AVG(DISTANCE_KM) AS DISTANCE_KM
        , AVG(WEIGHT_MT) AS WEIGHT_MT
        , AVG(FUEL_PRICE_INDEX) AS FUEL_PRICE_INDEX
    FROM base 
    GROUP BY 
        ORIGINAL_BASE_POINT
        , DESTINATION_BASE_POINT
        , TRANSPORT_MODE
)
, merge_result AS (
    SELECT
        l3m.ORIGINAL_BASE_POINT
        , l3m.DESTINATION_BASE_POINT
        , l3m.TRANSPORT_MODE
        , l3m.DESTINATION_RISK_AVG
        , l3m.WEATHER_CONDITION
        , l3m.LEAD_TIME_DAYS
        , l3m.DISRUPTION_RATE
        , wp.DISTANCE_KM
        , wp.WEIGHT_MT
        , wp.FUEL_PRICE_INDEX
    FROM latest_3_month AS l3m
    LEFT JOIN whole_period AS wp
    USING (ORIGINAL_BASE_POINT, DESTINATION_BASE_POINT, TRANSPORT_MODE)
)
SELECT * FROM merge_result