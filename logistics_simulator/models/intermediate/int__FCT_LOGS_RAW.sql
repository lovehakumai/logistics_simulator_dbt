WITH base AS (
    SELECT
        SHIPMENT_ID
        , DATE
        , UPPER(ORIGIN_PORT) AS ORIGIN_PORT
        , UPPER(DESTINATION_PORT) AS DESTINATION_PORT
        , UPPER(TRANSPORT_MODE) AS TRANSPORT_MODE
        , PRODUCT_CATEGORY
        , DISTANCE_KM
        , WEIGHT_MT
        , FUEL_PRICE_INDEX
        , GEOGRAPHICAL_RISK_SCORE
        , WEATHER_CONDITION
        , CARRIER_RELIABILITY_SCORE
        , LEAD_TIME_DAYS
        , DISRUPTION_OCCURRED
    FROM {{ref('stg_RAW__GLOBAL_SUPPLY_CHAIN_RISK_2024_2026')}}
)
, PORT_MST AS (
    SELECT
        ID 
        , COUNTRY_CD_2CHAR	
        , COUNTRY_CD_3CHAR	
        , COUNTRY_NAME	
        , BASE_POINT	
        , TRANSPORT_MODE	
        , PORT_NAME_JP	
        , PORT_NAME_EN	
        , LATITUDE	
        , LONGITUDE
    FROM {{ref('stg_DEV_SEEDS__MAIN_PORTS')}}
)
, base_with_port AS(
    SELECT 
        SHIPMENT_ID
        , DATE
        , ORIGIN_PORT AS ORIGIN_BASE_POINT
        , ORIGIN.COUNTRY_CD_2CHAR AS ORIGIN_COUNTRY_CD_2CHAR
        , ORIGIN.COUNTRY_CD_3CHAR AS ORIGIN_COUNTRY_CD_3
        , ORIGIN.COUNTRY_NAME AS ORIGIN_COUNTRY_NAME
        , ORIGIN.PORT_NAME_JP AS ORIGIN_PORT_NAME_JP
        , ORIGIN.PORT_NAME_EN AS ORIGIN_PORT_NAME_EN
        , ORIGIN.LATITUDE AS ORIGIN_LATITUDE
        , ORIGIN.LONGITUDE AS ORIGIN_LONGITUDE

        , DESTINATION_PORT AS DESTINATION_BASE_POINT
        , DESTINATION.COUNTRY_CD_2CHAR AS DESTINATION_COUNTRY_CD_2
        , DESTINATION.COUNTRY_CD_3CHAR AS DESTINATION_COUNTRY_CD_3
        , DESTINATION.COUNTRY_NAME AS DESTINATION_COUNTRY_NAME
        , DESTINATION.PORT_NAME_JP AS DESTINATION_PORT_NAME_JP
        , DESTINATION.PORT_NAME_EN AS DESTINATION_PORT_NAME_EN
        , DESTINATION.LATITUDE AS DESTINATION_LATITUDE
        , DESTINATION.LONGITUDE AS DESTINATION_LONGITUDE

        , base.TRANSPORT_MODE
        , base.PRODUCT_CATEGORY
        , base.DISTANCE_KM
        , base.WEIGHT_MT
        , base.FUEL_PRICE_INDEX
        , base.GEOGRAPHICAL_RISK_SCORE
        , base.WEATHER_CONDITION
        , base.CARRIER_RELIABILITY_SCORE
        , base.LEAD_TIME_DAYS
        , base.DISRUPTION_OCCURRED
    FROM base 
    LEFT JOIN PORT_MST AS ORIGIN 
    ON base.ORIGIN_PORT = ORIGIN.BASE_POINT
        AND base.TRANSPORT_MODE = ORIGIN.TRANSPORT_MODE
    LEFT JOIN PORT_MST AS DESTINATION
    ON base.DESTINATION_PORT = DESTINATION.BASE_POINT
        AND base.TRANSPORT_MODE = DESTINATION.TRANSPORT_MODE
)
{# SELECT * FROM base_with_port #}
SELECT DISTINCT WEATHER_CONDITION FROM base_with_port